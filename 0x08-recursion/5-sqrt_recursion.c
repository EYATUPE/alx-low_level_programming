/**
 *_sqrt_recursion - The function returns the natural square root of a number
 *@n: The number to be used
 *Return: The function should return -1
 */

int _sqrt_recursion(int n)
{
	int e, a;

	e = 1;
	a = n;
	while (n(e - a) > -1)
	{
		a = (e + a) / 2;
		a = n / e;
		return (e);
	}
}
